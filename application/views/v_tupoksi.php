TUGAS, FUNGSI DAN URAIAN TUGAS CAMAT <br>

Bagian Kesatu Camat <br>

Pasal 2 <br>

(1)	Kecamatan dibentuk dalam rangka meningkatkan koordinasi penyelenggaraan pemerintahan, pelayanan publik dan pemberdayaanmasyarakat Desa dan Kelurahan. <br>
(2)	Camat dalam melaksanakan tugas sebagaimana dimaksud pada ayat (1) menyelanggarakan fungsi : <br>
a.	penyelenggaraan urusan pemerintahan umum; <br>
b.	pengoordinasian kegiatan pemberdayaan masyarakat; <br>
c.	pengoordinasian upaya penyelenggaraan ketentraman dan ketertiban umum; <br>
d.	pengoordinasian penerapan dan penegakan Perda dan Peraturan Bupati; <br>
e.	pengoordinasian pemeliharaan prasarana dan sarana pelayanan umum; <br>
f.	pengoordinasian penyelenggaraan kegiatan pemerintahan yang dilakukan oleh perangkat daerah di tingkat kecamatan; <br>
g.	pembinaan dan pengawasan penyelenggaraan kegiatan Desa dan Kelurahan; <br>
h.	pelaksanaan urusan pemerintahan yang menjadi kewenangan daerah yang tidak dilaksanakan oleh unit kerja pemerintah daerah yang ada di kecamatan; <br>
i.	pelaksanaan fungsi lain yang diberikan oleh Bupati terkait dengan tugas dan fungsinya. <br>

(3)	Untuk menjabarkan tugas dan fungsi sebagaimana dimaksud pada ayat (1) dan ayat (2), Camat mempunyai uraian tugas sebagai berikut: <br>
a.	merumuskan program kerja dan kegiatan Kecamatan dalam wilayah kerjanya; <br>
b.	menyelenggarakan urusan pemerintahan umum; <br>
c.	mengoordinasikan kegiatan pemberdayaan masyarakat; <br>
d.	mengoordinasikan upaya penyelenggaraan ketentraman dan ketertiban umum; <br>
e.	mengoordinasikan penerapan dan penegakan Perda dan Perbup; <br>
f.	mengoordinasikan	pemeliharaan	prasarana	dan	sarana pelayanan umum; <br>
g.	mengoordinasikan penyelenggaraan kegiatan pemerintahan yang dilakukan oleh perangkat daerah di tingkat Kecamatan; <br>
h.	membina dan mengawasi penyelenggaraan kegiatan Desa dan/atau Kelurahan;
i.	Melaksanakan	fasilitasi,	penetapan,	pembinaan, pengawasan, rekomendasi, evaluasi terhadap Pemerintahan Desa. <br>
j.	melaksanakan urusan pemerintahan yang menjadi kewenangan daerah yang tidak dilaksanakan oleh unit kerja Pemerintah Daerah yang ada di Kecamatan; <br>
k.	melaksanakan sebagian kewenangan Bupati yang dilimpahkan; <br>
l.	koordinasi dan kerjasama dengan instansi terkait; <br>
m.	pemantauan, evaluasi dan pelaporan;dan <br>
n.	melaksanakan tugas lain yang diberikan oleh atasan sesuai peraturan perundang-undangan yang berlaku. <br>

Pasal 3 <br>

Unsur-unsur organisasi Kecamatan terdiri dari : <br>
a.	Sekretariat; <br>
b.	Seksi Pemerintahan; <br>
c.	Seksi Pelayanan Umum; <br>
d.	Seksi Pemberdayaan Masyarakat; <br>
e.	Seksi Ketentraman dan Ketertiban Umum; <br>
f.	Seksi Pembinaan dan Pengawasan Pemerintahan Desa; <br>
g.	Kelompok Jabatan Fungsional. <br>

Bagian Kedua Sekretariat <br>

Pasal 4 <br>

(1)	Sekretariat mempunyai tugas menyelenggarakankoordinasi pelaksanaan tugas, pembinaan dan pemberian dukungan administrasi kepada seluruh unit organisasi. <br>
(2)	Sekretariat dalam melaksanakan tugas sebagaimana dimaksud pada ayat (1) menyelenggarakan fungsi: <br>
a.	koordinasi kegiatan perencanaan, pelaksanaan, evaluasi dan pelaporan; <br>
b.	penyusunan rencana, program kerja dan anggaran; <br>
c.	pembinaan dan pemberian dukungan administrasi yang meliputi ketatausahaan, kepegawaian, keuangan, kerumahtanggaan, perlengkapan, kerja sama, hubungan masyarakat dan kearsipan; <br>
d.	pembinaan organisasi dan tata laksana Kecamatan; <br>
e.	koordinasi dan penyusunan Peraturan Perundang- undangan; <br>
f.	penyelenggaraan pengelolaan barang milik daerah/negara; <br>
g.	pengelolaan kegiatan pemantauan, evaluasi, dokumentasi dan pelaporan; <br>
h.	pengumpulan, pengelolaan data dan teknologi informasi; <br>
i.	koordinasi penyelesaian tindak lanjut hasil pemeriksaan; <br>
j.	koordinasi dan kerjasama dengan instansi terkait; <br>
k.	pemantauan, evaluasi dan pelaporan;dan <br>
l.	pelaksanaan tugas lain yang diberikan oleh atasansesuai bidang tugas. <br>

(3)	Untuk menjabarkan tugas dan fungsi sebagaimana dimaksud pada ayat (1) dan ayat (2), Sekretariat mempunyai uraian tugas sebagai berikut: <br>
a.	melaksanakan	koordinasi	kegiatan	dengan	unit	kerja dilingkungan Kecamatan; <br>
b.	melaksanakan penyusunan rencana, program kerja dan anggaran Kecamatan; <br>
c.	menyelenggarakan	pelaksanaan	urusan	tata	laksana keuangan; <br>
d.	melaksanakan urusan perbendaharaan dan gaji; <br>
e.	melaksanakan urusan verifikasi dan akuntansi keuangan; <br>
f.	melakukan penyusunan laporan keuangan; <br>
g.	menyiapkan bahan dan pelaksanaan urusan kepegawaian; <br>
h.	menyiapkan bahan pembinaan pegawai; <br>
i.	menyiapkan	bahan	dan	pelaksanaan	urusan ketatausahaan dan kearsipan; <br>
j.	menyiapkan bahan dan pelaksanaan urusan rumahtangga dan perlengkapan; <br>
k.	menyiapkan bahan dan pelaksanaan urusan kerja sama dan kehumasan; <br>
l.	melaksanakan pengelolaan dan inventarisasi barang milik daerah/negara; <br>
m.	melaksanakan layanan pengadaan Kecamatan; <br>
n.	melaksanakan pembinaan organisasi dan tata laksana Kecamatan; <br>
o.	melaksanakan	koordinasi	dan	penyusunan	Peraturan Perundang-undangan; <br>
p.	menghimpun	dan	menyiapkan	bahan	penyusunan laporan; <br>
q.	menyelenggarakan pengelolaan kegiatan dokumentasi dan informasi; <br>
r.	melaksanakan	pengumpulan,	pengelolaan	data	dan penyelenggaraan urusan teknologi informasi; <br>
s.	melaksanakan koordinasi dan kerjasama dengan instansi terkait; <br>
t.	melaksanakan	pemantauan,	evaluasi	dan	pelaporan pelaksanaan tugas; dan <br>
u.	melaksanakan  tugas  lain  yang  diberikan  oleh	atasan sesuai bidang tugas; <br>

Pasal 5 <br>

Sekretariat terdiri dari: <br>
a.	Sub Bagian Umum dan Kepegawaian; dan <br>
b.	Sub Bagian Perencanaan, Keuangan dan Aset. <br>
 
Pasal 6 <br>

(1)	Sub Bagian Umum dan Kepegawaian mempunyai tugas melaksanakan kegiatan pengelolaan urusan ketatausahaan, rumahtangga, perlengkapan, kepegawaian, organisasi dan kehumasan. <br>
(2)	Untuk menjabarkan tugas sebagaimana dimaksud pada ayat (1), Sub Bagian Umum dan Kepegawaian mempunyai uraian tugas sebagai berikut: <br>
a.	menyusun program dan rencana kerja urusan umum dan kepegawaian kecamatan; <br>
b.	menyusun pedoman dan petunjuk teknis pelaksanaan kegiatan urusan umum dan kepegawaian; <br>
c.	melaksanakan	kegiatan	ketatausahaan	termasuk persuratan dan kearsipan kecamatan; <br>
d.	melaksanakan	fasilitasi	penyusunan	peraturan perundang-undangan;
e.	melaksanakan kegiatan rumah tangga kecamatan; <br>
f.	melaksanakan pengelolaan administrasi pendistribusian sarana alat kantor dan keperluan alat kantor terhadap unsur-unsur unit organisasi; <br>
g.	melaksanakan pengelolaan urusan keamanan dan kebersihan lingkungan kantor; <br>
h.	melakukan penyimpanan, pemilahan, pemindahan dan penjadualan retensi serta penyusutan arsip; <br>
i.	melaksanakan pelayanan administrasi perjalanan dinas, akomodasi tamu, kehumasan dan keprotokolan serta kerjasama; <br>
j.	melaksanakan pengelolaan sistem informasi dan pelayanan informasi. <br>
k.	menyiapkan bahan pembinaan pegawai, meliputi pembinaan disiplin, pengawasan melekat, kesejahteraan, pemberian tanda jasa/ penghargaan dan kedudukan hukum pegawai; <br>
l.	melaksanakan penataan administrasi kepegawaian yang meliputi bazzeting, formasi, DUK, data pegawai, pengarsipan berkas pegawai dan rekapitulasi absensi pegawai; <br>
m.	menghimpun bahan usulan mutasi kepegawaian, meliputi pengusulan, kepangkatan dalam jabatan, kenaikan pangkat, kenaikan gaji berkala, pemindahan, pemberhentian, pensiun dan cuti; <br>
n.	menyiapkan bahan penyampaian Laporan Harta Kekayaan Penyelenggara Negara (LHKPN) dan  Laporan  Harta Kekayaan Aparatur Sipil Negara (LHKASN); <br>
o.	menyiapkan bahan penyampaian Laporan Pajak-Pajak Pribadi (LP2P) Aparatur Sipil Negara Kecamatan; <br>
p.	melaksanakan pemantauan pelaksanaan tata naskah dan tata kearsipan sesuai dengan petunjuk  dan  ketentuan yang berlaku; <br>
q.	menyiapkan bahan pembinaan, penataan organisasi dan tata laksana Kecamatan; <br>
r.	menyiapkan bahan koordinasi dan kerjasama dengan instansi terkait dalam rangka pelaksanaan tugas; <br>
 
s.	melaksanakan pemantauan, evaluasi dan pelaporan pelaksanaan tugas; dan <br>
t.	melaksanakan tugas lain yang diberikan oleh atasan sesuai dengan bidang tugas <br>

