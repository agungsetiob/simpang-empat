Bagian Ketiga Seksi Pemerintahan <br>

Pasal 8 <br>
(1)	Seksi Pemerintahan mempunyai tugas melaksanakan kegiatan koordinasi dan penyelenggaraan urusan pemerintahan serta urusan pemerintahan umum berdasarkan pelimpahan kewenangan dari Pemerintah Kabupaten kepada camat. <br>
(2)	Untuk menjabarkan tugas sebagaimana dimaksud pada ayat (1), Seksi Pemerintahan mempunyai uraian tugas sebagai berikut: <br>

a.	menyusun program dan rencana kerja urusan pemerintahan yang menjadi kewenangan; <br>
b.	melaksanakan koordinasi/sinergi dengan perangkat daerah dan instansi terkait dalam rangka penyelenggaraan urusan pemerintahan dan urusan pemerintahan umum sesuai kewenangan; <br>

c.	perencanaan dan pelaksanaan kegiatan pemerintahan; <br>
d.	melaksanakan koordinasi dengan perangkat daerah atau instansi terkait dalam rangka pemeliharaan sarana dan prasarana pelayanan umum; <br>
e.	melaksanakan pemeliharaan prasarana dan fasilitas pelayanan umum yang melibatkan pihak swasta; <br>
f.	melaskanakan urusan pemerintahan umum yang menjadi kewenangan kecamatan atau berdasarkan pelimpahan kewenangan pemerintah kabupaten, <br>
g.	menyelenggarakan	urusan	pemerintahan	yang kewenangannya dilimpahkan kepada camat; <br>
h.	melaksanakan urusan pemerintahan lain yang dilimpahkan kepada camat; <br>
i.	menyiapkan bahan pengoordinasian penyelenggaraan kegiatan pemerintahan di tingkat kecamatan; <br>
j.	menyiapkan bahan koordinasi dan kerjasama dengan instansi terkait dalam rangka pelaksanaan tugas; <br>
k.	melaksanakan pemantauan, evaluasi dan pelaporan pelaksanaan tugas; dan <br>
l.	melaksanakan tugas lain yang diberikan oleh atasan sesuai dengan bidang tugasnya. <br>
 
Bagian Keempat Seksi Pelayanan Umum <br>
Pasal 10 <br>

(1)	Seksi Pelayanan Umum mempunyai tugas melaksanakan urusan Pemerintahandi Kecamatan berdasarkan pelimpahan kewenangan urusan Pemerintah Kabupaten di bidang pelayanan umum. <br>
(2)	Untuk menjabarkan tugas sebagaimana dimaksud  pada  ayat (1), Seksi Pelayanan umummempunyai uraian tugas sebagai berikut: <br>
a.	menyusun program dan rencana kerja urusan pemerintahan di bidang pelayanan umum yang menjadi kewenangan; <br>
b.	menyusun pedoman dan petunjuk teknis pelaksanaan kegiatan pelayanan umum di kecamatan; <br>
c.	melaksanakan fasilitasi percepatan pencapaian standar pelayanan minimal di wilayah kecamatan; <br>
d.	melaksanakan peningkatan efektifitas pelaksanaan pelayanan kepada masyarakat di wilayah kecamatan; <br>
e.	melaksanakan urusan pemerintahan yang dilimpahkan kepada camat meliputi : <br>
1.		urusan pemerintahan di bidang pelayanan perizinan non usaha; <br>
2.		urusan pemerintahan di bidang pelayanan nonperizinan; dan <br>
3.		urusan pemerintahan di bidang pelayanan kepada masyarakat yang dilimpahkan; <br>
f.	melaksanakan koordinasi/sinergi pelaksanaan kegiatan di bidang pelayanan dengan instansi terkait; <br>
g.	penyelenggaraan pelayanan administrasi terpadu kecamatan (Paten); <br>
h.	menyiapkan bahan dan melaksanakan pelayanan masyarakat yang menjadi ruang lingkup tugasnya dan/atau yang belum dapat dilaksanakan oleh pemerintahan desa/kelurahan; <br>
i.	menyiapkan bahan koordinasi dan kerjasama dengan instansi terkait dalam rangka pelaksanaan tugas; <br>
j.	melaksanakan pemantauan, evaluasi dan pelaporan pelaksanaan tugas; dan <br>
k.	melaksanakan tugas lain yang diberikan oleh atasan sesuai dengan bidang tugasnya. <br>

Bagian Keempat <br>
Seksi Ketentraman dan Ketertiban Umum Pasal 9 <br>
(1)	Seksi Ketentraman dan Ketertiban Umum mempunyai tugas melaksanakan tugas pemerintah kecamatan berdasarkan pelimpahan kewenangan dari pemerintah kabupaten dibidang keamanan dan ketertiban umum yang meliputi pembinaan keamanan dan ketertiban umum serta penegakan Peraturan Perundang-undangan. <br>
 

(2)	Untuk menjabarkan tugas sebagaimana dimaksud  pada  ayat (1), Seksi Ketentraman dan Ketertiban Umum mempunyai uraian tugas sebagai berikut: <br>
a.	menyusun program dan rencana kerja urusan pemerintahan di bidang ketentraman dan ketertiban umum yang menjadi kewenangan Kecamatan; <br>
b.	menyusun pedoman dan petunjuk teknis pelaksanaan kegiatan ketentraman dan ketertiban umum yang menjadi kewenangan Kecamatan; <br>
c.	melaksanakan koordinasi/sinergi dengan perangkat daerah dan/atau instansi terkait dalam pelaksanaan ketentraman dan ketertiban umum; <br>
d.	melaksanakan fasilitasi dan kegiatan ketentraman dan ketertiban umum sesuai pelimpahan wewenang pemerintah kabupaten, diantaranya : <br>
1.	pembinaan ketentraman dan ketertiban umumdi lingkungan kecamatan dan desa/kelurahan; <br>
2.	pembinaan ketertiban pemanfaatan fasilitas umum dan fasilitas	pemerintahan	di	kecamatan	dan desa/kelurahan; <br>
3.	pembinaan ketentraman kehidupan warga masyarakat di kecamatan, desa/kelurahan; <br>
4.	pembinaan Siskamling di desa/kelurahan;dan <br>
5.	pembinaan penegakan  peraturan  perundang-undangan di kecamatan, desa/kelurahan; <br>
e.	melaksanakan koordinasi upaya penyelenggaraan ketentraman dan ketertiban umum; <br>
f.	melaksanakan koordinasi penerapan dan penegakan peraturan daerah dan peraturan kepala daerah; <br>
g.	menyiapkan bahan dan melaksanakan pelayanan masyarakat di kecamatan, desa/kelurahan sesuai bidang tugasnya; <br>
h.	menyiapkan bahan koordinasi dan kerjasama dengan instansi terkait dalam rangka pelaksanaan tugas; <br>
i.	melaksanakan pemantauan, evaluasi dan pelaporan pelaksanaan tugas; dan <br>
j.	melaksanakan tugas lain yang diberikan oleh atasan sesuai dengan bidang tugasnya. <br>

Bagian Kelima <br>
Seksi Pemberdayaan Masyarakat Pasal 11 <br>
(1)	Seksi Pemberdayaan Masyarakat mempunyai tugas melaksanakan tugas dibidang pemberdayaan masyarakat yang menjadi kewenangan kecamatan/berdasarkan pelimpahan kewenangan pemerintah kabupaten. <br>
(2)	Untuk menjabarkan tugas sebagaimana dimaksud  pada  ayat (1), Seksi Pemberdayaan Masyarakat mempunyai uraian tugas sebagai berikut: <br>
 
a.	menyusun program dan rencana kerja urusan pemerintahan di bidang pemberdayaan masyarakat yang menjadi kewenangan Kecamatan; <br>
b.	menyusun pedoman dan petunjuk teknis pelaksanaan kegiatan pemberdayaan masyarakat yang menjadi kewenangan Kecamatan; <br>
c.	melaksanakan koordinasi/sinergi dengan perangkat daerah dan/atau instansi terkait dalam pelaksanaan pemberdayaan masyarakat; <br>
d.	melaksanakan koordinasi kegiatan pemberdayaan,meliputi
: <br>
1.	peningkatan parsisipasi masyarakat dalam forum musyawarah perencanaan pembangunan di desa; <br>
2.	Sinkronisasi Program Kerja dan Kegiatan Pemberdayaan Masyarakat yang Dilakukan oleh Pemerintah dan Swasta di Wilayah Kerja Kecamatan <br>
3.	Peningkatan Efektifitas Kegiatan Pemberdayaan Masyarakat di Wilayah Kecamatan <br>

e.	melaksanakan koordinasi kegiatan pemberdayaan kelurahan, meliputi : <br>

1.	Peningkatan	Partisipasi	Masyarakat	dalam	Forum Musyawarah Perencanaan Pembangunan di Kelurahan. <br>
2.	Pembangunan Sarana dan Prasarana Kelurahan <br>
3.	Evaluasi Kelurahan; <br>
f.	melaksanakan koordinasi dan fasilitasi pelaksanaan kegiatan di bidang pemberdayaan masyrakat sesuai dengan pelimpahan wewenang pemerintah kabupaten yang meliputi; <br>
1.	pembinaan ketenagakerjaan dan transmigrasi; <br>
2.	pembinaan keluarga berencana; <br>
3.	pembinaan	pemberdayaan	keluarga,	masyarakat, perempuan dan perlindungan anak; <br>
4.	pembinaan kesehatan masyarakat dan posyandu; <br>
5.	pembinaan penyandang masalah kesejahteraan sosial (PMKS), keluarga kurang mampu, dll; <br>
6.	pembinaan keluarga sejahtera; dan <br>
7.	pemberian bantuan kesejahteraan sosial dan korban bencana; <br>
8.	melaksanakan penyelenggaraan kegiatan bidang keagamaan; <br>
9.	pembinaan perdagangan,industri, koperasi dan usaha mikro; <br>
10.	pembinaan pertanian (tanaman pangan, peternakan, perkebunan, kehutanan, kelautan dan perikanan), energi dan sumber daya mineral, lingkungan hidup, perhubungan, komunikasi dan informatika serta penanaman modal;
11.	pembinaan penyuluhan pertanian dan ketahanan pangan;dan <br>
12.	menyiapkan bahan pelaksanaan pelayanan masyarakat yang menjadi ruang lingkup tugasnya dan/atau yang belum dapat dilaksanakan oleh pemerintahan desa/kelurahan; <br>
 
g.	menyiapkan	bahan	koordinasi	dan	kerjasama	dengan instansi terkait dalam rangka pelaksanaan tugas; <br>
h.	melaksanakan	pemantauan,	evaluasi	dan	pelaporan pelaksanaan tugas; dan <br>
i.	melaksanakan tugas lain yang diberikan oleh atasan sesuai dengan bidang tugasnya. <br>

Bagian Ketujuh <br>
Seksi Pembinaan dan Pengawasan Pemerintahan Desa <br>
Pasal 12 <br>
(1)	Seksi Pembinaan dan Pengawasan Pemerintahan Desa mempunyai tugas melaksanakan tugas di bidang pembinaan dan pengawasan pemerintahan desa sesuai dengan kewenangan; <br>
(2)	Untuk menjabarkan tugas sebagaimana dimaksud  pada  ayat (1), Seksi Pembinaan dan Pengawasan Pemerintahan Desa mempunyai uraian tugas sebagai berikut: <br>
a.	menyusun program dan rencana kerja urusan pemerintahan di bidang pembinaan dan pengawasan pemerintahan desa yang menjadi kewenangan Kecamatan; <br>
b.	menyusun pedoman dan petunjuk teknis pelaksanaan kegiatan pembinaan dan pengawasan pemerintahan desa yang menjadi kewenangan Kecamatan; <br>
c.	melaksanakan koordinasi/sinergi dengan perangkat daerah dan/atau instansi terkait  dalam  pelaksanaan  pembinaan dan pengawasan pemerintahan desa; <br>
d.	melaksanakan fasilitasi, rekomendasi dan koordinasi pembinaan dan pengawasan pemerintahan desa, meliputi; <br>
1.	fasilitasi penyusunan peraturan desa dan peraturan kepala desa <br>
2.	fasilitasi administrasi tata pemerintahan desa <br>
3.	fasilitasi pengelolaan keuangan desa dan pendayagunaan aset desa <br>
4.	fasilitasi	penerapan	dan	penegakan	peraturan perundang-undangan; <br>
5.	fasilitasi pelaksanaan tugas kepala desa dan perangkat desa; <br>
6.	fasilitasi pelaksanaan pemilihan kepala desa <br>
7.	fasilitasi	pelaksanaan	tugas	dan	fungsi	badan permusyawaratan desa <br>
8.	rekomendasi	pengangkatan	dan	pemberhentian perangkat desa <br>
9.	fasilitasi sinkronisasi perencanaan pembangunan daerah dengan pembangunan desa <br>
10.	fasilitasi	penetapan	lokasi	pembangunan	kawasan perdesaan <br>
11.	fasilitasi penyelenggaraan ketenteraman dan ketertiban umum. <br>
12.	fasilitasi	pelaksanaan	tugas,	fungsi,	dan	kewajiban lembaga kemasyarakatan; <br>
13.	fasilitasi	penyusunan	perencanaan	pembangunan partisipatif <br>
 
14.	fasilitasi kerja sama antardesa dan kerja sama desa dengan pihak ketiga <br>
15.	fasilitasi penataan, pemanfaatan, dan pendayagunaan ruang desa serta penetapan dan penegasan batas desa <br>
16.	fasilitasi	penyusunan	program	dan	pelaksanaan pemberdayaan masyarakat desa <br>
17.	koordinasi pendampingan desa di wilayahnya <br>
18.	koordinasi	pelaksanaan	pembangunan	kawasan perdesaan di wilayah kecamatan <br>
e.	menyiapkan bahan pelaksanaan pelayanan masyarakat yang menjadi ruang lingkup tugasnya dan/atau yang belum dapat dilaksanakan oleh pemerintahan desa/kelurahan; <br>
f.	menyiapkan bahan koordinasi dan kerjasama dengan instansi terkait dalam rangka pelaksanaan tugas; <br>
g.	melaksanakan pemantauan, evaluasi dan pelaporan pelaksanaan tugas; dan <br>
h.	melaksanakan tugas lain yang diberikan oleh atasan sesuai dengan bidang tugasnya. <br>

